                                    <p>

                                        El Congreso General de la Cultura Guna es
                                        el máximo organismo de expresión religiosa,
                                        de protección, conservación y divulgación del
                                        patrimonio histórico-cultural del pueblo guna.
                                        Reconocido bajo la ley fundamental de Gunayala
                                        y de fiel cumplimiento por las 49 comunidades
                                        de Gunayala, representadas en sus saglagan,
                                        en sus argamar y en sus suaribgan. <br><br>


                                        Los Congresos Culturales Guna, surge de la Nación Guna,
                                        una nación altamente organizada mucho antes de 1492.
                                        Emanaba en las faldas de las montañas y riberas de
                                        los grandes ríos (Dagargunyala, Duilewala, Gosgunwala, entre otros),
                                        hoy nuestros territorios se encuentran ubicados
                                        por la frontera de Panamá y Colombia.
                                        Con la llegada de Ibeorgun y Giggadiryai
                                        se organiza la estructura del régimen
                                        político de la nación guna, la cual
                                        se hace cuando los 9 nelegan dirigen y
                                        asumen en la práctica de la organización
                                        y estructuración de los Congresos Culturales Guna
                                        realizada entre varias lunas en los afluentes
                                        de Duilewala, epicentro de los Congresos.
                                        <br><br>


                                        Después de 1492, con la invasión y etnocidio del
                                        colonialismo español a Abiayala (América),
                                        la Nación Guna, hizo su gran esfuerzo de
                                        mantener sus congresos y, gracias a la unidad,
                                        organización y valentía de nuestros sabios nelegan
                                        y de los dirigentes se mantuvo tal cohesión en
                                        tiempos difíciles durante la colonia. <br><br>


                                        En esa época, los Congresos Culturales Guna fue
                                        guiado por Iguabi, Iguasalibler, y otros dirigentes
                                        que eran duligan, urrigan y otros. El Congreso se
                                        mantuvo y su objetivo principal fue la de mantener
                                        un territorio propio de los guna lo que se logró,
                                        aunque mínimamente. Muchas escenas de sacrificio y
                                        heroísmo no se han escrito, los cuales aún quedan
                                        en la memoria de nuestros ancianos. <br><br>



                                        De esa singular lucha de resistencia podemos
                                        escribir tomos de libros con respecto a estos
                                        acontecimientos; como el decreto de 1871 donde
                                        gracias a los Congresos Guna se logró conquistar
                                        “La Comarca Tule Nega", dentro del Departamento
                                        de Panamá que en ese entonces, estaba regido por
                                        el gobierno colombiano. Pero fue con
                                        Inaggailibaler (Abisua), el gran absogued,
                                        cuando se fortalece y consolidan nuevamente
                                        los Congresos Guna, cuando ya la Nación Guna,
                                        se asientan en las islas del Caribe. <br><br>




                                        Después de la separación de Panamá de Colombia
                                        surgieron dos (2) Congresos Generales Guna,
                                        uno dirigido por lnabaginya y el otro por
                                        Ologindibibilele (Simral Colman).
                                        Este conflicto duro varios años,
                                        hasta que en 1945 en la comunidad
                                        de Dubwala (Tubualá), nuevamente se
                                        unifica la Nación Guna al mando de
                                        Yabiliginya, Olotebiliginya y Estanislao López. <br><br>


                                        Este Congreso se caracterizó más en la
                                        defensa territorial. Así como el
                                        desarrollo sin control de las estructuras
                                        de la sociedad occidental sin sentido,
                                        no hubo un programa adecuado bajo la
                                        orientación de la cultura guna, lo cual
                                        trajo un aislamiento en la lucha por el
                                        desarrollo del patrimonio cultural y
                                        del pensamiento guna; más bien era un
                                        Congreso Político-Administrativo
                                        de la Nación Guna. <br><br>



                                        Por ello, en 1971, un grupo de dirigentes
                                        guna defensores de la cultura guna. Promueven
                                        entre el Congreso General Guna, celebrado en
                                        Dubwala la celebración y reestructuración de
                                        un Congreso General de la Cultura Guna,
                                        la cual sería un organismo religioso y
                                        de rescate y divulgación del patrimonio
                                        cultural de la Nación Guna, para que
                                        nuestro pensamiento milenario quedé a
                                        la par con las otras culturas del mundo. <br><br>


                                        Fue en 1972 cuando se celebró el Primer
                                        Congreso General de la Cultura Guna a nivel
                                        "Comarcal" en la población de Gardi Sugdub.
                                        Como su primer Sagladummad fue Ologawigdiginya.
                                        En los primeros 5 congresos se reestructuran
                                        los objetivos que servirían de orientación, y
                                        se redacta un reglamento para el buen funcionamiento. <br><br>



                                        El Onmaggeddummad Namaggaled o Congreso General de la Cultura Guna,
                                        empezó a desarrollarse paulatinamente. Ahora sus sesiones
                                        regulares son de 6 días hábiles,
                                        hay reuniones de coordinación. <br><br>




                                        Hoy el Congreso General de la Cultura Guna,
                                        juega un papel muy importante en el rescate,
                                        desarrollo y divulgación de nuestra
                                        cultura guna, enarbolando más que nunca
                                        la bandera de la lucha por la identidad,
                                        autonomía, desarrollo y defensa de nuestro
                                        patrimonio cultural, tesoro
                                        legado por nuestros ancestros gunadule.






                                    </p>
















