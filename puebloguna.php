                                    <p>

                                        Hablar de toda la historia del pueblo guna nos
                                        llevaría a escribir un extenso libro, que partiría
                                        desde su origen como pueblo y de los procesos de
                                        lucha de nuestros grandes líderes espirituales.
                                        Pero, en este escrito solo se plasmará su relación
                                        de contacto en el periodo colonial con los
                                        españoles y piratas franceses e ingleses.
                                        Como también de los diversos procesos o
                                        intentos de evangelización, pacificación,
                                        reducción y exterminio de la Nación Guna
                                        por parte de los españoles. Puesto que el
                                        guna se ha caracterizado por ser un pueblo aguerrido, rebelde y que ha luchado
                                        por su identidad, autonomía y autodeterminación.<br><br>


                                        1501. Rodrigo de Bastidas, primer español que
                                        recorre las costas de Gunayala.<br><br>

                                        1514. Se funda el poblado español, Acla (Ailadiwar denominado en Guna)
                                        en la costa de Gunayala, cerca de Goedupu. <br><br>

                                        1611. Debido a los atropellos causados por
                                        los españoles, los guna se levantan en armas
                                        y atacan al poblado español: Chepo.
                                        A partir de entonces, se iniciaría un
                                        sinnúmero de ataques por parte del
                                        pueblo guna tanto a poblados como
                                        a soldados españoles en el Darién. <br><br>


                                        1615. El poblado de El Real de
                                        San Miguel (río Bayano) es atacada por los guna.<br><br>


                                        1617. Sublevan los guna entre Chepo y Puertos Pinos.
                                        Atacando una hacienda en el río de la Maestra y
                                        otro en el área de Corozal.<br><br>


                                        1620-21. Se crea el Gobierno de Darién para
                                        controlar el área y reducir los constantes
                                        ataques de los guna. Es nombrado como Gobernador
                                        a Francisco de Maldonado y Saavedra,
                                        quien organiza en España una expedición
                                        al Darién para su colonización, pero que
                                        posteriormente fracasaría. <br><br>


                                        1637-38. Se inician los pactos de "pacificación y reducción” de los guna,
                                        a través de Julián Carrisolio de Alfaráz con la colaboración
                                        de Fray Adrián de Santo Tomás, por los constantes
                                        ataques de los guna a las poblaciones españoles.
                                        A la vez, con Fray se iniciaría la evangelización
                                        de la Nación Guna, empezando así, la aculturación
                                        e imposición de la cultura española sobre los guna.<br><br>


                                        1647. Debido al incumplimiento de aquellos pactos,
                                        nuevamente los guna se levantan en armas atacando
                                        a diversos poblados españolas. <br><br>


                                        1675. El poblado de Chepo es ocupado por
                                        los guna en alianza con el pirata francés
                                        La Sound, iniciándose con ello una mayor
                                        vinculación histórica entre ambos grupos
                                        étnicos, como también con los piratas
                                        ingleses en sus acciones contra los españoles. <br><br>


                                        1680. La guarnición y el poblado de El Real de Santa María,
                                        es atacada y saqueada, acción realizada conjuntamente
                                        entre los guna y piratas ingleses.
                                        Y nuevamente la guarnición es atacada en 1684.
                                        En tal sentido, los españoles firman un
                                        tratado de paz y de alianza con los guna.
                                        La alianza es falsa y no dura, y a
                                        finales del siglo XVII, los guna
                                        se enemistan nuevamente con los españoles. <br><br>



                                        1685. Se estudia la reducción de los guna en el Darién,
                                        para que no ayuden a los piratas, su destierro en otras
                                        partes de América o su exterminio. Recomendado por un
                                        Consejo de Guerra reunido en Madrid. A su vez,
                                        recomienda la conquista del Darién por una armada
                                        española, el exterminio y el poblamiento nuevo. <br><br>



                                        1698-1700. En la costa de Gunayala se funda la
                                        colonia Escocesa de Nueva Caledonia.
                                        El sitio sería conocido como Puerto Escoces,
                                        no muy lejos de la comunidad de Goedupu.
                                        Pero fracasará este intento de colonización
                                        tanto en 1698 y 1699, y se abandonará la
                                        colonia definitivamente en 1700. <br><br>








