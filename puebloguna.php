                                    <p>

                                        La identidad cultural guna tiene un significado profundo y
                                        complejo desde su cosmología, que significa ANMAR DAED,
                                        literalmente quiere decir, comportamiento,
                                        (actitud de los guna, forma de ser).
                                        Son las raíces que fortalecen la actitud, conducta y
                                        comportamiento de los guna de forma colectiva. <br><br>


                                        A diferencia de la cultura no guna, donde el sistema
                                        cultural ha sido impactado por la economía neoliberal tiende
                                        ser más egoísta, es decir, el rico cuando se hace
                                        cada vez más rico cae en el egoísmo y al individualismo.
                                        Mientras, el guna cuando trabaja más, produce más y
                                        comparte más con la familia y con la comunidad.
                                        El guna cazador cuando trae un "macho monte" u
                                        otro animal, lo comparte con la comunidad,
                                        pero el contacto con los no guna ha permitido
                                        que estos valores se estén perdiendo. <br><br>

                                        La identidad cultural guna está constituida por
                                        el sistema integrado y coherente de valores,
                                        instituciones, historia, lengua, costumbres,
                                        tradiciones y espiritualidad propia, que conforman
                                        el fundamento de la identidad del pueblo guna y
                                        que se manifiestan a través de su filosofía,
                                        arte y sistema socio-político, creado y desarrollado
                                        por el gunadule a través de los siglos. <br><br>

                                        Igualmente, está vinculado al Babigala
                                        (Camino de Baba (Padre) y Nana (Madre)).
                                        Relatos verbales que son divulgadas a
                                        través de cantos y que a su vez se convierten
                                        en historias que explican a Baba y Nana, sobre
                                        la creación del universo y la Madre Tierra,
                                        hasta la definición del ser humano, su papel
                                        en el camino y cuidado de la Madre Tierra. <br><br>


                                        El pueblo guna transmiten su identidad cultural y otros
                                        conocimientos por vía oral a través de los saglagan e
                                        interpretado por los argargan en los Onmaggednega
                                        (Casa del Congreso), lugar donde se reúnen todas las
                                        personas de una comunidad gunadule a debatir los
                                        problemas del pueblo y escuchar el Babigala.

                                        Los guna, poseen un profundo sentido de identidad
                                        cultural y gran conciencia del valor de su modo de
                                        vida; mantienen vivas sus tradiciones, como el uso
                                        de su lengua y cantos, su espíritu y filosofía de
                                        la vida, las cuales guardan una estrecha relación
                                        con el bosque, la tierra y el agua.<br><br>



                                        Por otro lado, en la actualidad ejerce su educación
                                        desde sus conocimientos ancestrales para comprender
                                        su pasado, presente y futuro a través del Babigala.
                                        Su autonomía y autogobierno actual e identidad
                                        cultural y filosofía se fundamentan en ellos
                                        para construir su organización y su unidad. <br><br>



                                        Por consiguiente, los elementos que constituyen
                                        la identidad cultural guna son las siguientes:
                                        el conocimiento sagrado, el idioma, la indumentaria
                                        (vestuario), los cantos y relatos sagrados,
                                        las costumbres, las Tradiciones, la forma de
                                        organización, las normas de control social,
                                        la religión y la espiritualidad, los símbolos,
                                        la música y danza, la forma de autoridad,
                                        las artesanías, las escrituras o pictografía.






