                            <h2 class=" fs-1  text-center mt-3 "> <b> HISTORIA DE LOS CONGRESOS CULTURALES GUNA</b>
                            </h2>
                            <p><i>Fecha de publicación: 5/1/2023</i></p>
                            <div class=" bg-bloquegunayala p-2  card-text-guna ">
