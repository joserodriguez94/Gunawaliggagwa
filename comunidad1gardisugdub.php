                                                    gar bila dulegan, que habitan en los remolinos (biria) del cuarto
                                                    nivel,
                                                    bajo de la desembocadura del río. Cuando nuestros antepasados
                                                    llegaron a
                                                    la costa se manifestaban a menudo, pero los neles lograron
