                                                    años
                                                    cambiaron otra vez de lugar y fueron viniendo hacia aquí. Llegaron a
                                                    Missibe. Le llamaron así porque más abajo, en el primer nivel,
                                                    residían
                                                    espíritus malos parecidos a gatos. Los antepasados trabajaron duro
                                                    de
                                                    nuevo, tumbaron los árboles, cultivaron guineo. Tenían de todo. Bab
                                                    Dummat les ayudaba. Todavía no habían llegado al mar y las islas
                                                    seguían
                                                    deshabitadas. De ahí nuestros antepasados fueron a Nabugana y luego
                                                    a
                                                    Sapdurbiria, cerca del mar. Los wagas ya estaban en la costa en esa
                                                    época. Nuestros antepasados iban al mar para pescar, las islas
                                                    estaban
                                                    despobladas, y no tenían dueño. Los peces abundaban y no tenían
                                                    miedo de
                                                    los humanos, había jureles, sábalos, muchas tortugas, etc. En la
                                                    costa
                                                    los animales tampoco nos temían, había muchas langostas, cangrejos…
