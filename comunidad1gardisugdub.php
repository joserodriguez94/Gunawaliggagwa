                                                    sábalo, el pargo, el jurel, los cangrejos y las tortugas. Nuestros
                                                    antepasados estaban contentos, no les faltaba de nada. Tenían
                                                    pescado,
                                                    langosta, tortuga, los nainus (fincas) estaban cerca, eran de Bab
