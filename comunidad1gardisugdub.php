                                                    Los
                                                    hombres de Coibita, como los de Coiba, eran muy agresivos. Como una
                                                    parte de Aspandup se hundió con el maremoto, cruzaron a la isla
                                                    vecina:
                                                    Yandup. Anteriormente, cuando Aspandup era grande y nuestros
                                                    antepasados
                                                    hacían vida ahí, Yandup era el cementerio. Pero después Yandup pasó
                                                    a
                                                    ser el pueblo grande.
                                                    Año tras año fue creciendo, había todo tipo de especialistas: sia
                                                    tuled
                                                    dummat, gabur tulet, abosget, dubaibet. En esa época Sugdup todavía
