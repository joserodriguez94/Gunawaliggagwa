                                        están alejadas de la costa a una y a dos horas aproximadamente de caminata. <br><br>



                                        El territorio de Gunayala está dividido en 4 corregimientos a saber:
                                        Corregimiento número 1, que se conoce como el Corregimiento de Nargana
                                        el cual integran 28 comunidades. Corregimiento número 2,
                                        al cual se le conoce como el Corregimiento de Agligandi
                                        (Ailigandi) y se compone de 11 comunidades;
                                        Corregimiento número 3, que se conoce como el Corregimiento de Dubwala (Tubualá)
                                        y lo integran 10 comunidades y finalmente, el Corregimiento número 4,
                                        que se conoce como el Corregimiento de Puerto Obaldía que lo integran dos
                                        comunidades: Puerto Obaldía y La Miel, las cuales sus pobladores son no
                                        gunadule, no indígenas, en los últimos años se ha registrado que
                                        estas personas en su mayoría provienen de Colombia. <br><br>




                                        La Comarca Gunayala, se extiende por el Norte, el Mar Caribe,
