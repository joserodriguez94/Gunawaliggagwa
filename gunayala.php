                                        La Comarca Gunayala, es uno de los seis pueblos gunadule que existen entre
                                        Panamá y Colombia localizada en el sector nororiental de la República de
                                        Panamá en la costa del Mar Caribe o también conocida como el Archipiélago
                                        de Gunayala ( reconocido igualmente como San Blas, denominación antiguo
                                        del territorio ) y otras localizadas en el lado colombiano. Tiene una extensión
                                        territorial de 3 mil 206 km2, sin incluir la parte marítima que aproximadamente
                                        podría totalizar unos 5 mil km2. <br> <br>

                                        En cuanto el origen paleográfico de la región, la
                                        formación del istmo Costa Rica-Panamá se inició por actividades
                                        volcánicas durante el período Jurásico-Superior
                                        (desde 190 millones hasta 136 millones de años atrás).<br> <br>

                                        Durante el período Cretácico Superior
                                        (entre 136 y 165 millones de años atrás)
                                        se formó un archipiélago de islas volcánicas.
                                        Este se extendía desde el noreste de Colombia
                                        hasta Nicaragua. La cordillera de Gunayala, así
                                        como las serranías de Maje, Pirre, Bagre y Sapo
                                        en el Darién y las Islas de las Perlas, están
                                        constituidas por los núcleos de esas antiguas
                                        islas volcánicas, que se levantaron por las
                                        actividades tectónicas del Eoceno. <br> <br>

                                        La geología superficial de la región se caracteriza
                                        por un predominio de productos del proceso volcánico
                                        y acciones erosivas posteriores, combinado
                                        con sedimentaciones marinas. En cuanto a
                                        recursos minerales de la región,
                                        se ha encontrado una variedad de minerales
                                        económicamente importantes: oro, manganeso y cobre (mineralizado). <br><br>








