                                        El Gobierno de Gunayala se encuentra bajo el dominio de los Congresos
                                        Generales Guna (Onmaggeddummagan) y esta a su vez se divide en dos:
                                        el Congreso General de la Cultura Guna (Onmaggeddummad Namaggaled),
                                        autoridad máxima de carácter cultural-espiritual y
                                        el Congreso General Guna (Onmaggeddummad Sumaggaled),
                                        autoridad máxima de carácter político-administrativo.
                                        Cada Congreso cuenta con 3 Sagladummagan o los llamados
                                        Caciques de los Congresos Generales de Gunayala. <br> <br>

                                        De acuerdo con las nuevas reglas, la permanencia de Sagladummagan
                                        (caciques) de Onmaggeddummad Sumaggaled (Congreso General Guna)
                                        en el cargo son de 5 años, reelegibles. Hace poco, la salida
                                        dependía de su gestión que era evaluado por la asamblea cada
                                        cierto tiempo, su condición física o de salud, o cualquier
                                        falta que se considere grave que amerite su destitución,
                                        o Sagladummad por su cuenta renuncie al cargo. Con las
                                        nuevas normas la era de periodo indefinido para
                                        Sagladummagan de Onmaggeddummad Sumaggaled terminó.
                                        A diferencia del Onmaggeddummad Namaggaled (Congreso de la Cultura Guna),
                                        se mantiene el periodo indefinido de sus líderes en el cargo. <br> <br>

                                        Las comunidades del territorio están sujetas a estas máximas
                                        autoridades que rigen de acuerdo a los límites y competencias
                                        trazados en la Ley Fundamental de Gunayala
