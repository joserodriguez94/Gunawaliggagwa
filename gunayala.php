                                    <p>

                                        La Comarca Gunayala, creada por la Ley Segunda del 16 de septiembre de 1938,
                                        pero nombrada en aquel momento como San Blas, luego declarada
                                        Reserva por la Ley 20 del 31 de enero de 1957, después denominada Comarca Kuna Yala
                                        en la Ley 99 del 23 de diciembre de 1998,
                                        y designada como Comarca Gunayala en el año 2010.
                                        Sin embargo, este último nombre no es ley nacional,
                                        no obstante, es reconocido por el estado panameño y la nación misma. <br> <br>

                                        Las primeras escrituras del idioma guna que se conoce vienen
                                        de parte de aquellas personas que no hablaban el idioma.
                                        Personas que hablaban un idioma distinto al guna.
                                        De acuerdo con los registros históricos desde que el
                                        europeo llegó a nuestras tierras circularon: españoles,
                                        ingleses, escoceses, austriacos, holandeses, franceses,
                                        entre otros, quienes tenían estructuras lingüísticas
                                        totalmente distintas al nuestro. <br> <br>

                                        Entre los primeros exploradores a quienes se
                                        atribuyen las escrituras del idioma guna se menciona a
                                        Lionel Wafer de 1681, cuando apareció en un poblado gunadule.
                                        En 1748, estuvo el austriaco, Padre Jacobo Walburger.
                                        Y Dr. Gullen en 1849, que además de los números escribió
                                        nombres de lugares. <br> <br>

                                        Las primeras palabras del idioma guna que escribió Wafer,
                                        fueron de los números. Naturalmente, Wafer escribió
                                        dichas palabras tal como él la escuchaba. De allí,
                                        inicia las primeras distorsiones que incide en sus
                                        pronunciaciones. Hecho que aún hoy ocurre, cuando los
                                        “no hablantes guna”, desde las estructuras gubernamentales
                                        oficializan las distorsiones que, a diferencia de ellos,
                                        los de ayer, procuraban escribir tal como ellos
                                        escuchaban, no las traducciones. <br> <br>

                                        Desde esta época hasta 1908, un poco más de 200 años,
                                        según los documentos históricos la escritura que
                                        impusieron al nombre del pueblo gunadule fue CUNA,
                                        escrita con C, no con K. Si no es hasta que,
                                        llegó el padre Leonardo Gassó en 1908 a Gunayala
                                        cuando se conoce la escritura Karibe-Kuna,
                                        escritas con K. <br> <br>


                                        Con Leonardo Gassó se habló de 19 letras para escribir
                                        el guna, luego otro grupo habló de 16 letras,
                                        otro con 23 y 25 letras. Posteriormente, ya con
                                        la intervención de profesionales gunadule en el
                                        año 1975 se plantea las 18 letras, luego 19.
                                        Finalmente, bajo el sello de los Congresos Generales
                                        a través de un equipo de técnicos compuesto de estudiosos,
                                        investigadores y lingüistas gunadule, el alfabeto
                                        se redujo a 15 letras, quedando como guna con G.
                                        El cual fue oficializado con la Ley 88 del 22 de
                                        noviembre, 2010. No obstante, no la letra G,
                                        sino se mantuvo con la K. Luego las autoridades guna,
                                        oficializa la letra G en la región,
                                        este último cambio solo es reconocido
                                        en la Comarca Gunayala y no por el
                                        estado panameño, pero de forma indirecta
                                        las instituciones públicas
                                        reconocen la letra G. <br> <br>

                                        A continuación, se mencionarán algunos de los términos
                                        más escuchados en la Comarca Gunayala. <br>


                                    <ul>

                                        <li class=" mb-3">
                                            <p> <em> Onmaggeddummad Namaggaled (Congreso General de la Cultura Guna) </em> </p>
                                        </li>

                                        Autoridad máxima de Gunayala de carácter cultural y de espiritualidad.




                                        <li class=" mt-4 mb-3">
                                            <p> <em> Onmaggeddummad Sunmaggaled (Congreso General Guna) </em></p>
                                        </li>
                                        Autoridad máxima de Gunayala de carácter político y administrativo.


                                        <li class=" mt-4 mb-3">
                                            <p><em>Onmageddummagan (Congresos Generales Guna)</em></p>
                                        </li>

                                        Autoridad que surge de la unión de Onmaggeddummad
                                        Namaggaled y Onmaggeddummad Sunmaggaled.
                                        Las dos autoridades máximas de Gunayala se complementan
                                        en la formación de la identidad personal y
                                        colectiva de los guna, y en la defensa de su territorio.



                                        <li class=" mt-4 mb-3">
                                            <p> <em>Neggwebur Onmagged (Congreso Local)</em></p>
                                        </li>

                                        Primera autoridad de cada comunidad o pueblo.
                                        Se constituye con la participación plena de los
                                        miembros de cada comunidad o pueblo de Gunayala.


                                        <li class=" mt-4 mb-3">
                                            <p><em> Neggwebur / Gwebur (Comunidad) </em></p>
                                        </li>

                                        Conglomerado de personas que habitan en un territorio
                                        (isla o parte continental)
                                        delimitado bajo la autoridad de un sagla y onmaggednega
                                        (la casa del congreso),
                                        con características de autonomía
                                        relativa con referencia a sus similares,
                                        y reconocida como tal por los Onmaggeddummagan.


                                        <li class=" mt-4 mb-3">
                                            <p> <em>Sagladummad / Sagladummagan (Cacique General / Caciques Generales)</em></p>
                                        </li>

                                        Persona o personas que representan legalmente a las autoridades máximas de
                                        Gunayala que son: Onmaggeddummad Namaggaled y
                                        el Onmaggeddummad Sunmaggaled, que se mueven de
                                        acuerdo a competencias establecidas en la
                                        Ley Fundamental, en el Estatuto y en los
                                        reglamentos de los congresos generales.



                                        <li class=" mt-4 mb-3">
                                            <p> <em>Sagla / Saglagan (Cacique o Caciques)</em></p>
                                        </li>

                                        Persona o personas que representa la autoridad principal
                                        en cada comunidad o pueblo, que durante la sesión de
                                        Onmaggeddummad Sunmaggaled,
                                        lleva el voto de su pueblo al plenario.

                                        <li class=" mt-4 mb-3">
                                            <p> <em>Delegado / Delegados </em></p>
                                        </li>

                                        Persona o personas elegidas en el pleno de cada congreso local, y que representan a su comunidad
                                        en las deliberaciones de las sesiones de los Onmaggeddummagan.


                                        <li class=" mt-4 mb-3">
                                            <p> <em>Babigala (Historia Guna) </em></p>
                                        </li>

                                        Conjunto de tratados que fundamentan la fe del pueblo guna.
                                        Particularmente, de la Comarca Gunayala.

                                        <li class=" mt-4 mb-3">
                                            <p> <em>Onmaggednega (La casa del congreso)</em> </p>
                                        </li>
                                        Lugar donde se reúnen todos los comuneros de un pueblo
                                        para debatir sobre la comunidad y escuchar el Babigala.


                                        <li class=" mt-4 mb-3">
                                            <p><em> Argar / Argarmar (intérprete)</em> </p>
                                        </li>

                                        Persona o personas que interpreta las narraciones del Babigala
                                        cantadas por el sagla o saglagan.
                                        Puesto que los cantos son dichas
                                        en simbolismo que solo este personaje puede entender.


                                        <li class=" mt-4 mb-3">
                                            <p> <em>Suwaribed / Suwaribgan (Guardián / Guardianes )</em></p>
                                        </li>

                                        persona o personas que se encargan de la disciplina y
                                        orden en la comunidad, especialmente, en las
                                        ceremonias de cantos tradicionales.



                                        <li class=" mt-4 mb-3">
                                            <p><em> Absoged (Epidemiólogo)</em></p>
                                        </li>

                                        Especialista con capacidad de aplacar epidemias en las comunidades.



                                        <li class=" mt-4 mb-3">
                                            <p><em> Nele / Nelegan (Vidente / Videntes)</em> </p>
                                        </li>



                                        Personaje o personajes que diagnostican enfermedades, predice e interpreta acontecimientos.




                                    </ul>
